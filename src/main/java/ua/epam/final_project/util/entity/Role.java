package ua.epam.final_project.util.entity;

public class Role {

    private int id;
    private String roleEn;
    private String roleUa;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRoleEn() {
        return roleEn;
    }

    public void setRoleEn(String roleEn) {
        this.roleEn = roleEn;
    }

    public String getRoleUa() {
        return roleUa;
    }

    public void setRoleUa(String roleUa) {
        this.roleUa = roleUa;
    }
}
